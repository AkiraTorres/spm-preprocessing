def sort_general_info_csv(csv_path: str = "outputs/results/1/general_info.csv", sep: str = ";") -> None:
    """Ordena o CSV pela coluna 'scenery' e adiciona 's_support_percentage' e 's_support_std_percentage'.

    - Ordenação: tenta numérica (1, 2, 10, ...); se falhar, usa lexicográfica.
    - 's_support_percentage' = (média de s_support / 2085) * 100.
    - 's_support_std_percentage' = (desvio padrão de s_support / 2085) * 100.
      Se 's_support' vier como string no formato "média (+- desvio)", extrai ambos.
    - Sobrescreve o arquivo no mesmo caminho.
    """
    if not os.path.exists(csv_path):
        raise FileNotFoundError(f"Arquivo não encontrado: {csv_path}")

    df = pd.read_csv(csv_path, sep=sep)
    if "scenery" not in df.columns:
        raise KeyError("Coluna 'scenery' não encontrada no CSV")

    # calcular s_support_percentage e s_support_std_percentage, se possível
    if "s_support" in df.columns and "total_sequences" in df.columns:
        # extrai parte numérica da coluna s_support (suporta "12.34 (+- 1.23)" ou já numérico)
        if not pd.api.types.is_numeric_dtype(df["s_support"]):
            s_as_str = df["s_support"].astype(str)
            # extrai média e desvio, se presentes
            mean_std = s_as_str.str.extract(r"^\s*([\d]+[\.,]?[\d]*)\s*(?:\(\+\-\s*([\d]+[\.,]?[\d]*)\))?", expand=True)
            mean_str = mean_std[0].str.replace(",", ".", regex=False)
            std_str = mean_std[1].str.replace(",", ".", regex=False)
            s_num = pd.to_numeric(mean_str, errors="coerce")
            s_std = pd.to_numeric(std_str, errors="coerce").fillna(0)
        else:
            s_num = pd.to_numeric(df["s_support"], errors="coerce")
            s_std = pd.Series([0] * len(df), index=df.index, dtype="float64")

        total_before_mining = 2085.0
        df["s_support_percentage"] = (s_num / total_before_mining) * 100
        df["s_support_percentage"] = df["s_support_percentage"].fillna(0).round(4)
        df["s_support_std_percentage"] = (s_std / total_before_mining) * 100
        df["s_support_std_percentage"] = df["s_support_std_percentage"].fillna(0).round(4)

    scenery_num = pd.to_numeric(df["scenery"], errors="coerce")
    if scenery_num.notna().any():
        df["_scenery_num"] = scenery_num
        df = df.sort_values(by=["_scenery_num", "scenery"], ascending=True)
        df = df.drop(columns=["_scenery_num"])
    else:
        df = df.sort_values(by="scenery", ascending=True)

    df.to_csv(csv_path, sep=sep, index=False)


if __name__ == "__main__":
    path = sys.argv[1] if len(sys.argv) > 1 else "outputs/results/1/general_info.csv"
    sort_general_info_csv(path)


